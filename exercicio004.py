v1 = input('Digite algo: ')
print('O tipo primitivo desse valor é {}'.format(type(v1)))
print('Só tem espaços? {}'.format(v1.isspace()))
print('É um número? {}'.format(v1.isnumeric()))
print('É alfanumérico? {}'.format(v1.isalnum()))
print('Esta em maiúscula? {}'.format(v1.isupper()))
print('Esta em minúscula? {}'.format(v1.islower()))
print('Esta capitalizada? {}'.format(v1.istitle()))




